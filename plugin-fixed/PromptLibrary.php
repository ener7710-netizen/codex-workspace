<?php
declare(strict_types=1);

namespace SEOJusAI\AI;

defined('ABSPATH') || exit;

/**
 * Centralized, bias-safe prompt templates for legal SEO.
 */
final class PromptLibrary {

    public static function pageType(string $text): string {
        return <<<PROMPT
Ты — нейтральный SEO-классификатор для юридического сайта.
Твоя задача — выбрать ОДНУ наиболее подходящую категорию.

Текст страницы:
"""
{$text}
"""

Возможные категории (равнозначны, без приоритета):
- Услуга адвоката
- Статья / разбор закона
- Судебная практика / кейс
- FAQ / вопрос-ответ
- Новости / изменения законодательства
- О компании / адвокате
- Контакты / запись на консультацию

Ответь строго в формате:
Категория: <одна категория>
Уверенность: <число от 0 до 1>
PROMPT;
    }

    public static function practiceArea(string $text): string {
        return <<<PROMPT
Ты — юридический классификатор.
Определи основную область права, к которой относится текст.
Если текст смешанный, выбери доминирующую.

Текст:
"""
{$text}
"""

Возможные области (все равновероятны):
- Уголовное право
- Гражданское право
- Семейное право
- Арбитраж и корпоративные споры
- Административное право
- Налоговое право
- Трудовое право
- Недвижимость и земельные споры
- Иное

Ответ:
Область права: <одна область>
Обоснование (1 предложение):
PROMPT;
    }

    public static function searchIntent(string $text): string {
        return <<<PROMPT
Ты — SEO-аналитик юридических сайтов.
Определи поисковый интент страницы.

Текст:
"""
{$text}
"""

Возможные интенты:
- Коммерческий
- Информационный
- Навигационный
- Смешанный

Ответ:
Интент: <один вариант>
PROMPT;
    }

    public static function seoTitle(string $text): string {
        return <<<PROMPT
Ты — SEO-редактор юридического сайта.
Создай SEO-Title длиной до 60 символов.

Ограничения:
- нейтральный юридический стиль
- без маркетинговых клише
- без обещаний результата

Текст:
"""
{$text}
"""

Ответ:
SEO Title:
PROMPT;
    }

    public static function metaDescription(string $text): string {
        return <<<PROMPT
Ты — SEO-редактор адвокатского сайта.
Напиши meta description (120–160 символов).

Ограничения:
- без гарантий исхода дела
- без давления на пользователя
- информативно и нейтрально

Текст:
"""
{$text}
"""

Ответ:
Meta description:
PROMPT;
    }
}
public static function multiLabel(string $text): string {
return "Ты — классификатор без предпочтений.\nТекст:\n{$text}";
}
